<template>
    <form @submit.prevent="submit">
        <table>
            <tr>
                <td>아이디</td>
                <td><input type="text" v-model="userId"></td>
            </tr>
            <tr>
                <td>비밀번호</td>
                <td><input type="password" v-model="userPw"></td>
            </tr> 
            <tr>
                <td>사용자명</td>
                <td><input type="text" v-model="userName"></td>
            </tr>
            <tr>
                <td>직업</td>
                <td>
                    <select v-model="job">
                        <option v-for="jobCode in jobCodes" :value="jobCode.value" :key="jobCode.value">
                            {{ jobCode.label }}
                        </option>
                    </select>
                </td>
            </tr>
        </table>
        <div>
            <button type="submit">회원가입</button>
        </div>
    </form>
</template>

<script>
export default {
    name: 'SignupForm',
    props: {
        jobCodes: {
            type: Array
        }
    },
    data() {
        return {
            userId: '',
            userName: '',
            userPw: '',
            job: '00'
        }
    },
    methods: {
        submit(){
            const { userId, userName, userPw, job } = this
            this.$emit('submit', { userId, userName, userPw, job })
        }
    }
}
</script>